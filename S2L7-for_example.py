
# range 1st arg: starting int
# range 2nd arg: ending int
# range 3rd arg: interval
print('My name is')
for i in range(5):
    print('Jimmy 5 times ' + str(i))

print('Ranged Jimmy')
for i in range(12, 14):
    print('Jimmy 5 times ' + str(i))

print('Ranged Skipping Jimmy')
for i in range(12, 27, 3):
    print('Jimmy 5 times ' + str(i))

print('Ranged Negatively Skipping Jimmy')
for i in range(0, -20, -2):
    print('Jimmy 5 times ' + str(i))
